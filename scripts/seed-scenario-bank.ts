function makeDocId(e: SeedEntry): string {
  const slug = (s: string) => s.slice(0, 25).replace(/[^a-zа-ш0-9]/gi, '_').toLowerCase();
  return `bro_${e.grade}_${slug(e.topicTitle)}_${slug(e.title)}`;
}

