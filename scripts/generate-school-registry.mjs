#!/usr/bin/env node
/**
 * scripts/generate-school-registry.mjs
 *
 * Generates data/schoolRegistry.ts from the official MK government school
 * registry files in scripts/data-sources/ (downloaded from
 * https://mon.gov.mk/mk-MK/dokumenti/lista-na-aktivni-registri — primary
 * schools, secondary schools, and regional vocational education/training
 * centers). Mirrors scripts/generate_grade_ts.cjs's "read raw data -> transform
 * -> write a typed data/*.ts file" pattern.
 *
 * Run with: node scripts/generate-school-registry.mjs
 *
 * The raw source files are committed alongside this script so the generation
 * is reproducible without depending on the government URLs staying live.
 */
import XLSX from 'xlsx';
import mammoth from 'mammoth';
import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.resolve(__dirname, '..');
const SOURCES = path.join(ROOT, 'scripts', 'data-sources');

function parseSpreadsheet(filePath, type) {
  const wb = XLSX.readFile(filePath);
  const rows = XLSX.utils.sheet_to_json(wb.Sheets[wb.SheetNames[0]], { header: 1, defval: '' });
  const dataRows = rows.slice(1); // skip header row
  const entries = [];
  let counter = 1;
  for (const row of dataRows) {
    const municipality = String(row[1] ?? '').trim();
    const name = String(row[2] ?? '').trim();
    const address = String(row[3] ?? '').trim();
    if (!name) continue;
    entries.push({
      id: `${type}-${String(counter).padStart(3, '0')}`,
      type,
      municipality,
      name,
      ...(address ? { address } : {}),
    });
    counter++;
  }
  return entries;
}

async function parseVetCenters(filePath) {
  const { value: text } = await mammoth.extractRawText({ path: filePath });
  const lines = text.split('\n').map(l => l.trim()).filter(Boolean);
  const entries = [];
  let counter = 1;
  for (const line of lines) {
    const match = line.match(/^(.+?)\s*[–-]\s*(\S+)\s+(https?:\/\/\S+)/);
    if (!match) continue;
    const [, name, city, website] = match;
    entries.push({
      id: `vet-${String(counter).padStart(3, '0')}`,
      type: 'vet',
      municipality: city.trim(),
      name: name.trim(),
      city: city.trim(),
      website: website.trim(),
    });
    counter++;
  }
  return entries;
}

async function main() {
  const primary = parseSpreadsheet(path.join(SOURCES, 'schools-primary.xlsx'), 'primary');
  const secondary = parseSpreadsheet(path.join(SOURCES, 'schools-secondary.xls'), 'secondary');
  const vet = await parseVetCenters(path.join(SOURCES, 'schools-vet-centers.docx'));

  const all = [...primary, ...secondary, ...vet];

  console.log(`Primary schools: ${primary.length}`);
  console.log(`Secondary schools: ${secondary.length}`);
  console.log(`VET centers: ${vet.length}`);
  console.log(`Total: ${all.length}`);

  const outputFile = path.join(ROOT, 'data', 'schoolRegistry.ts');
  const header = `/**
 * Official MK government school registry — primary schools, secondary schools,
 * and regional vocational education/training centers. Generated by
 * scripts/generate-school-registry.mjs from scripts/data-sources/*.
 *
 * Source: https://mon.gov.mk/mk-MK/dokumenti/lista-na-aktivni-registri
 * Regenerate after updating the source files: node scripts/generate-school-registry.mjs
 */

export interface SchoolRegistryEntry {
  /** Stable slug, e.g. 'primary-042' — generated once, never reordered. */
  id: string;
  type: 'primary' | 'secondary' | 'vet';
  municipality: string;
  name: string;
  /** Absent for VET centers (only name/city/website are available in the source). */
  address?: string;
  city?: string;
  website?: string;
}

`;
  const body = `export const SCHOOL_REGISTRY: SchoolRegistryEntry[] = ${JSON.stringify(all, null, 2)};\n`;
  fs.writeFileSync(outputFile, header + body, 'utf8');
  console.log(`\nWrote ${outputFile}`);
}

main().catch(err => {
  console.error(err);
  process.exit(1);
});
