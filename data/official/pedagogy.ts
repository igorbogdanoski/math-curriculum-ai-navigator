/**
 * Builds the pedagogical context string injected into AI lesson plan prompts.
 * Ensures AI follows official Macedonian lesson structure and Bloom's taxonomy.
 */
export function buildPedagogyPromptContext(): string {
  return `
ОФИЦИЈАЛНА СТРУКТУРА НА НАСТАВНИОТ ЧАС (Сценарио):
• Воведна активност (~8 мин): Активирање на претходно знаење, мотивирање, поставување на целите на часот.
• Главни активности (~27 мин): Обработка на новата содржина преку активни методи (групна работа, истражување, проблемски ситуации).
• Завршна активност (~5 мин): Систематизација, домашна работа.
• Рефлексија (~5 мин): Метакогнитивна проверка — Математички семафор / Излезна картичка 3-2-1 / Скала 1-5.

КОГНИТИВНИ НИВОА (Блумова таксономија) — секој час треба да содржи прашања на барем 3 нивоа:
• Ниво 4 (Анализирај): „Кои информации ги имаш, а кои ти недостасуваат?" | „Нацртај скица/дијаграм."
• Ниво 5 (Евалуирај): „Дали резултатот има смисла?" | „Провери пред да продолжиш."
• Ниво 6 (Креирај): „Напиши го општото правило." | „Смисли слична задача во нов контекст."

ВАЖНО: Наставникот НЕ дава готов следен чекор кога ученикот заглавил. Наместо тоа, поставува насочувачко прашање кое го чува когнитивниот предизвик.

ТЕХНИКИ ЗА РЕФЛЕКСИЈА (избери едена):
- Математички семафор: зелено/жолто/црвено кружче
- Излезна картичка 3-2-1: 3 чекори · 2 грешки · 1 совет
- Скала 1-5: самооценување со образложување
`.trim();
}
