// Maps from existing topic IDs (data/grade7.ts) to official theme IDs above
export const TOPIC_ID_TO_OFFICIAL: Record<string, string> = {
  'g7-topic-1': 'g7-off-1',
  'g7-topic-2': 'g7-off-2',
  'g7-topic-3': 'g7-off-3',
  'g7-topic-4': 'g7-off-4',
  'g7-topic-5': 'g7-off-5',
};

/** Formats official MoE subtopic data for a given topic as a RAG context string. */
export function getOfficialTopicEnrichment(topicId: string): string {
  const offId = TOPIC_ID_TO_OFFICIAL[topicId];
  if (!offId) return '';

  const subtopics = GRADE7_OFFICIAL_SUBTOPICS.filter(s => s.themeId === offId);
  if (subtopics.length === 0) return '';

  const totalHours = subtopics.reduce((acc, s) => acc + s.hours, 0);
  const distributed = offId === 'g7-off-2' || offId === 'g7-off-4' || offId === 'g7-off-5';

  let ctx = '\n\n### ОФИЦИЈАЛНА МОН 2025 ПРОГРАМА (VII одделение) ###\n';
  ctx += `Тема: ${subtopics[0].themeName} | Вкупно часови: ${totalHours}ч`;
  if (distributed) ctx += ' (распоредена низ целата учебна година)';
  ctx += '\n\nПодтеми и содржини:\n';

  for (const sub of subtopics) {
    ctx += `\n◆ ${sub.subtopicName} (${sub.hours}ч)\n`;
    for (const c of sub.concepts) ctx += `  • ${c}\n`;
    ctx += `  Стандарди: ${sub.assessmentStandards.join('; ')}\n`;
    ctx += `  Блумови нивоа: ${sub.bloomLevels.join(', ')}\n`;
  }

  ctx += '\nНАПОМЕНА: Не надминувај ги овие содржини. Задачите да се во контекст на Македонија (МКД, македонски градови, имиња).\n';
  ctx += '### КРАЈ НА ОФИЦИЈАЛНА ПРОГРАМА ###\n';
  return ctx;
}

export interface OfficialSubtopicDoc {
  id: string;
  grade: number;
  text: string;
  topicId: string;
  topicTitle: string;
  subtopicTitle: string;
}

/** Returns all subtopics as flat docs suitable for vector embedding. */
export function getOfficialSubtopicDocs(): OfficialSubtopicDoc[] {
  return GRADE7_OFFICIAL_SUBTOPICS.map(sub => ({
    id: `g7-${sub.themeId}-${sub.subtopicName.replace(/\s+/g, '-').toLowerCase()}`,
    grade: 7,
    topicId: sub.themeId,
    topicTitle: sub.themeName,
    subtopicTitle: sub.subtopicName,
    text: [
      `Одделение: VII | Тема: ${sub.themeName} | Подтема: ${sub.subtopicName} | Часови: ${sub.hours}`,
      `Содржини: ${sub.concepts.join('; ')}`,
      `Стандарди за оценување: ${sub.assessmentStandards.join('; ')}`,
    ].join('\n'),
  }));
}

