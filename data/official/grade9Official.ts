// ── Helpers (matching grade6/7 interface) ────────────────────────────────────

export function buildGrade9Context(subtopics: OfficialSubtopic9[]): string {
  const themes = new Map<string, OfficialSubtopic9[]>();
  for (const s of subtopics) {
    if (!themes.has(s.themeName)) themes.set(s.themeName, []);
    themes.get(s.themeName)!.push(s);
  }
  let ctx = '';
  for (const [theme, subs] of themes) {
    const totalHours = subs.reduce((acc, s) => acc + s.hours, 0);
    ctx += `\nТема: ${theme} | Вкупно часови: ${totalHours}ч`;
    for (const s of subs) {
      ctx += `\n  • ${s.subtopicName} (${s.hours}ч): ${s.concepts.slice(0, 2).join('; ')}`;
    }
  }
  return ctx;
}

