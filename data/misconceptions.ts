/**
 * Враќа листа на мисконцепции за дадена тема.
 * Matching е case-insensitive и partial (substring).
 *
 * @param topicTitle  Насловот на темата или концептот
 * @returns           Листа на мисконцепции, или [] ако нема match
 */
export function getMisconceptionsForTopic(topicTitle: string): string[] {
  if (!topicTitle) return [];
  const normalized = topicTitle.toLowerCase();

  // Try exact match first
  const exactKey = Object.keys(MISCONCEPTIONS).find(k => normalized === k);
  if (exactKey) return MISCONCEPTIONS[exactKey];

  // Fall back to substring match — first match wins
  const partialKey = Object.keys(MISCONCEPTIONS).find(
    k => normalized.includes(k) || k.includes(normalized)
  );
  return partialKey ? MISCONCEPTIONS[partialKey] : [];
}
